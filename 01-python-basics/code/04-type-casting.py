"""
Topic: Type Casting
Description: Examples of implicit and explicit type casting.
"""

# Implicit Type Casting
num = 10
price = 5.5

result = num + price

print(result)
print(type(result))

# Explicit Type Casting
age = int("20")
height = float("5.8")
marks = str(95)
status = bool(1)

print(age)
print(height)
print(marks)
print(status)