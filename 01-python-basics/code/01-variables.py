"""
Topic: Variables
Description: Basic examples of variables in Python.
"""

# Storing values
name = "Noor"
age = 20
height = 5.8
is_student = True

print(name)
print(age)
print(height)
print(is_student)

# Variable reassignment
age = 21
print(age)

# Dynamic typing
data = 100
print(type(data))

data = "Python"
print(type(data))

data = True
print(type(data))