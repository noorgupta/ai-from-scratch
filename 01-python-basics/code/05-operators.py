"""
Topic: Operators
Description: Examples of commonly used Python operators.
"""

a = 10
b = 3

# Arithmetic
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a // b)
print(a % b)
print(a ** b)

# Comparison
print(a > b)
print(a == b)

# Logical
print(True and False)
print(True or False)
print(not True)

# Assignment
a += 5
print(a)

# Membership
print("AI" in "AI From Scratch")

# Identity
x = [1, 2]
y = x

print(x is y)