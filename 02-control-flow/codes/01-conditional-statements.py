"""
Topic: Conditional Statements
Description: Examples of if, elif, and else.
"""

# if statement
age = 20

if age >= 18:
    print("Eligible to vote")

# if-else statement
marks = 45

if marks >= 40:
    print("Pass")
else:
    print("Fail")

# if-elif-else statement
score = 82

if score >= 90:
    print("Grade A")
elif score >= 75:
    print("Grade B")
elif score >= 60:
    print("Grade C")
else:
    print("Grade D")