"""
Topic: For Loop
Description: Examples of iterating using a for loop.
"""

# Using range()
for i in range(5):
    print(i)

print()

# Iterating through a string
for letter in "Python":
    print(letter)

print()

# Iterating through a list
languages = ["Python", "Java", "C++"]

for language in languages:
    print(language)